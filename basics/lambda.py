# instead of defining the function somewhere and calling it, we can use
# python's lambda functions, which are inline functions defined at the same place
# we use it. So we don't need to declare a function somewhere and revisit the code
# just for a single time use.

# They don't need to have a name, so they also called anonymous functions. We define
# a lambda function using the keyword lambda.


sum = lambda a,b : a+b

print(sum(1,2))



